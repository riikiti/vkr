def _gf_mult(a, b, mod=0x169):
    """Умножение в GF(2^8) с модулем x^8 + x^6 + x^5 + x^3 + 1."""
    result = 0
    while b:
        if b & 1:
            result ^= a
        a <<= 1
        if a & 0x100:
            a ^= mod
        b >>= 1
    return result & 0xFF


def _mds_mult(col):
    """Умножение вектора 4 байт на MDS-матрицу."""
    result = [0] * 4
    for i in range(4):
        for j in range(4):
            result[i] ^= _gf_mult(_MDS[i][j], col[j])
    return result


def _rs_mult(key_bytes_8):
    """Умножение 8 байт ключа на RS-матрицу → 4 байта."""
    result = [0] * 4
    for i in range(4):
        for j in range(8):
            result[i] ^= _gf_mult(_RS[i][j], key_bytes_8[j], 0x14D)
    return result


def _h_func(x_word, L_list, k):
    """Функция h из Twofish: преобразование 32-бит слова через S-блоки и MDS."""
    y = [(x_word >> (8 * i)) & 0xFF for i in range(4)]

    if k == 4:
        y[0] = _Q1[y[0]] ^ ((L_list[3] >> 0) & 0xFF)
        y[1] = _Q0[y[1]] ^ ((L_list[3] >> 8) & 0xFF)
        y[2] = _Q0[y[2]] ^ ((L_list[3] >> 16) & 0xFF)
        y[3] = _Q1[y[3]] ^ ((L_list[3] >> 24) & 0xFF)

    if k >= 3:
        y[0] = _Q1[y[0]] ^ ((L_list[2] >> 0) & 0xFF)
        y[1] = _Q1[y[1]] ^ ((L_list[2] >> 8) & 0xFF)
        y[2] = _Q0[y[2]] ^ ((L_list[2] >> 16) & 0xFF)
        y[3] = _Q0[y[3]] ^ ((L_list[2] >> 24) & 0xFF)

    y[0] = _Q0[_Q0[y[0]] ^ ((L_list[1] >> 0) & 0xFF)] ^ ((L_list[0] >> 0) & 0xFF)
    y[1] = _Q0[_Q1[y[1]] ^ ((L_list[1] >> 8) & 0xFF)] ^ ((L_list[0] >> 8) & 0xFF)
    y[2] = _Q1[_Q0[y[2]] ^ ((L_list[1] >> 16) & 0xFF)] ^ ((L_list[0] >> 16) & 0xFF)
    y[3] = _Q1[_Q1[y[3]] ^ ((L_list[1] >> 24) & 0xFF)] ^ ((L_list[0] >> 24) & 0xFF)

    y[0] = _Q1[y[0]]
    y[1] = _Q0[y[1]]
    y[2] = _Q1[y[2]]
    y[3] = _Q0[y[3]]

    z = _mds_mult(y)
    return z[0] | (z[1] << 8) | (z[2] << 16) | (z[3] << 24)


def _rotl32(val, n):
    n &= 31
    return ((val << n) | (val >> (32 - n))) & 0xFFFFFFFF


def _rotr32(val, n):
    n &= 31
    return ((val >> n) | (val << (32 - n))) & 0xFFFFFFFF


class _TwofishKey:
    """Развёрнутый ключ Twofish."""

    def __init__(self, key_bytes):
        k = len(key_bytes) // 8  # количество 64-битных слов (2 для 128, 4 для 256)

        # Разбиваем ключ на чётные/нечётные 32-битные слова
        Me = []
        Mo = []
        for i in range(k):
            off = i * 8
            Me.append(struct.unpack_from('<I', key_bytes, off)[0])
            Mo.append(struct.unpack_from('<I', key_bytes, off + 4)[0])

        # S-vector из RS-матрицы
        S_vec = []
        for i in range(k):
            off = i * 8
            S_vec.append(_rs_mult(list(key_bytes[off:off + 8])))

        # Конвертируем S_vec в 32-бит слова (обратный порядок)
        self.s_keys = []
        for i in range(k - 1, -1, -1):
            sv = S_vec[i]
            self.s_keys.append(sv[0] | (sv[1] << 8) | (sv[2] << 16) | (sv[3] << 24))

        # Генерируем подключи K[0..39]
        self.K = [0] * 40
        rho = 0x01010101
        for i in range(20):
            A = _h_func(2 * i * rho, Me, k)
            B = _rotl32(_h_func((2 * i + 1) * rho, Mo, k), 8)
            self.K[2 * i] = (A + B) & 0xFFFFFFFF
            self.K[2 * i + 1] = _rotl32((A + 2 * B) & 0xFFFFFFFF, 9)

        self.k = k

    def g_func(self, x):
        """Функция g: применяет key-dependent S-блоки + MDS."""
        return _h_func(x, self.s_keys, self.k)


def _twofish_encrypt_block(block, tf_key):
    """Зашифровать один 128-битный блок Twofish."""
    R0, R1, R2, R3 = struct.unpack('<4I', block)

    # Входное отбеливание
    R0 ^= tf_key.K[0]
    R1 ^= tf_key.K[1]
    R2 ^= tf_key.K[2]
    R3 ^= tf_key.K[3]

    for r in range(_ROUNDS):
        T0 = tf_key.g_func(R0)
        T1 = tf_key.g_func(_rotl32(R1, 8))
        # PHT
        F0 = (T0 + T1 + tf_key.K[8 + 2 * r]) & 0xFFFFFFFF
        F1 = (T0 + 2 * T1 + tf_key.K[9 + 2 * r]) & 0xFFFFFFFF

        R2 = _rotr32(R2 ^ F0, 1)
        R3 = _rotl32(R3, 1) ^ F1

        # Обмен пар
        R0, R1, R2, R3 = R2, R3, R0, R1

    # Отмена последнего обмена
    R0, R1, R2, R3 = R2, R3, R0, R1

    # Выходное отбеливание
    R0 ^= tf_key.K[4]
    R1 ^= tf_key.K[5]
    R2 ^= tf_key.K[6]
    R3 ^= tf_key.K[7]

    return struct.pack('<4I', R0, R1, R2, R3)


def _twofish_decrypt_block(block, tf_key):
    """Расшифровать один 128-битный блок Twofish."""
    R0, R1, R2, R3 = struct.unpack('<4I', block)

    # Снятие выходного отбеливания
    R0 ^= tf_key.K[4]
    R1 ^= tf_key.K[5]
    R2 ^= tf_key.K[6]
    R3 ^= tf_key.K[7]

    for r in range(_ROUNDS - 1, -1, -1):
        # R0, R1 — входы F-функции этого раунда (не менялись раундом)
        T0 = tf_key.g_func(R0)
        T1 = tf_key.g_func(_rotl32(R1, 8))
        F0 = (T0 + T1 + tf_key.K[8 + 2 * r]) & 0xFFFFFFFF
        F1 = (T0 + 2 * T1 + tf_key.K[9 + 2 * r]) & 0xFFFFFFFF

        # Обратные операции раунда
        R2 = _rotl32(R2, 1) ^ F0
        R3 = _rotr32(R3 ^ F1, 1)

        # Обмен пар (отмена свопа раунда)
        R0, R1, R2, R3 = R2, R3, R0, R1

    # Компенсация undo-last-swap из шифрования (17 свопов = 1 нечётный)
    R0, R1, R2, R3 = R2, R3, R0, R1

    # Снятие входного отбеливания
    R0 ^= tf_key.K[0]
    R1 ^= tf_key.K[1]
    R2 ^= tf_key.K[2]
    R3 ^= tf_key.K[3]

    return struct.pack('<4I', R0, R1, R2, R3)


def _xor_bytes(a, b):
    return bytes(x ^ y for x, y in zip(a, b))


class TwofishCipher(BaseCipher):
    """Twofish — блочный шифр, финалист AES. Режим CBC с PKCS7."""

    KEY_SIZE = 32    # 256 бит
    BLOCK_SIZE = 16  # 128 бит

    @property
    def name(self) -> str:
        return "TWOFISH"

    def generate_key(self) -> bytes:
        return get_random_bytes(self.KEY_SIZE)

    def encrypt(self, data: bytes, key: bytes) -> bytes:
        iv = get_random_bytes(self.BLOCK_SIZE)
        padded = pad(data, self.BLOCK_SIZE)
        tf_key = _TwofishKey(key)

        ct = bytearray()
        prev = iv
        for i in range(0, len(padded), self.BLOCK_SIZE):
            block = padded[i:i + self.BLOCK_SIZE]
            xored = _xor_bytes(block, prev)
            encrypted = _twofish_encrypt_block(xored, tf_key)
            ct.extend(encrypted)
            prev = encrypted

        return iv + bytes(ct)

    def encrypt_deterministic(self, data: bytes, key: bytes, iv: bytes = None) -> bytes:
        if iv is None:
            iv = b'\x00' * self.BLOCK_SIZE
        else:
            iv = iv[:self.BLOCK_SIZE].ljust(self.BLOCK_SIZE, b'\x00')
        padded = pad(data, self.BLOCK_SIZE)
        tf_key = _TwofishKey(key)

        ct = bytearray()
        prev = iv
        for i in range(0, len(padded), self.BLOCK_SIZE):
            block = padded[i:i + self.BLOCK_SIZE]
            xored = _xor_bytes(block, prev)
            encrypted = _twofish_encrypt_block(xored, tf_key)
            ct.extend(encrypted)
            prev = encrypted

        return bytes(ct)

    def decrypt(self, data: bytes, key: bytes) -> bytes:
        iv = data[:self.BLOCK_SIZE]
        ct = data[self.BLOCK_SIZE:]
        tf_key = _TwofishKey(key)

        pt = bytearray()
        prev = iv
        for i in range(0, len(ct), self.BLOCK_SIZE):
            block = ct[i:i + self.BLOCK_SIZE]
            decrypted = _twofish_decrypt_block(block, tf_key)
            pt.extend(_xor_bytes(decrypted, prev))
            prev = block

        return unpad(bytes(pt), self.BLOCK_SIZE)
